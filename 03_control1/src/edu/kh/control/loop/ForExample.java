package edu.kh.control.loop;

import java.util.Scanner;

//기능용 클래스
public class ForExample {

	Scanner sc = new Scanner(System.in);
	
	public void ex1() {
		//1~10 까지 콘솔에 출력하기
		
		for(int i = 1; i<=10 ;i++) {
			//(반복)수행될 코드 입력
			System.out.println(i + "x");
			
		}
		for(int d = 1; d<=10 ;d++) {
			System.out.print(d);
	}
		}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
